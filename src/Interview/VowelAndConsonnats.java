package Interview;


public class VowelAndConsonnats {

	public static void main(String[] args) {
		//a,e,i,o,u
		
		char c= 'e';
		if(c=='a' || c=='e' || c=='i' || c=='o' || c=='u')
		{
			System.out.println(c+ " is a vowel ");
		}
		else
		{
			System.out.println(c+ " is a consonant ");
		}

	}

}
