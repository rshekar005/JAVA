package com.operators;

public class ComparisionDemo 
{
	//Relation or Comparision returns true or false(==,!=,>,<,>=,<=)
	public static void main(String args[])
	{
		int a =10;
		int b= 20;
		
		System.out.println(a==b);
		System.out.println(a>b);
		System.out.println(a<b);
		System.out.println(a>=b);
		System.out.println(a<=b);
		System.out.println(a!=b);
	}

}
